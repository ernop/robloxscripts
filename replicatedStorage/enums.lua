function getAssetTypeCounts(avatar)
	local assetTypeCounts={}
	for _,asset in pairs(avatar['assets']) do
		local assetType=assetTypes[asset.assetType.id]
		if assetTypeCounts[assetType]==nil then
			assetTypeCounts[assetType]=1
		else
			assetTypeCounts[assetType]=assetTypeCounts[assetType]+1
		end
	end
	return assetTypeCounts
end

